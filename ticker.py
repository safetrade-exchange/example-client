class Ticker:
  def __init__(self, amount, avg_price, high, last, low, open, price_change_percent, volume):
    self.amount = amount
    self.avg_price = avg_price
    self.high = high
    self.last = last
    self.low = low
    self.open = open
    self.price_change_percent = price_change_percent
    self.volume = volume